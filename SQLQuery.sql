CREATE PROCEDURE uspCADASTRAR_GERAL

@FORMATED_ADDRESS VARCHAR(200),
@LAT VARCHAR(30),
@LONG VARCHAR(30)

AS
BEGIN

	INSERT INTO TB_GEOCODE VALUES(@FORMATED_ADDRESS,@LAT,@LONG)

END

