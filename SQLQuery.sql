CREATE DATABASE DB_GEOCODEAPI

USE DB_GEOCODEAPI


CREATE TABLE TB_GEOCODE(
COD INT PRIMARY KEY IDENTITY(1,1),
FORMATED_ADDRESS VARCHAR(200),
LAT VARCHAR(30),
LONG VARCHAR(30)
)

